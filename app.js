async function tryUnlock() {
  const val = document.getElementById('password-input').value;
  if (val !== VIEW_PASSWORD && val !== ADMIN_PASSWORD) {
    document.getElementById('lock-error').classList.remove('hidden');
    return;
  }
  document.getElementById('lock-screen').classList.add('hidden');

  if (val === ADMIN_PASSWORD) {
    activateAdmin();
  } else {
    document.getElementById('admin-btn').classList.add('hidden');
    const label = document.getElementById('logged-in-label');
    label.innerHTML = 'Logged in as: <strong>Best Dad Ever</strong>';
    label.classList.remove('hidden');
  }

  await loadCards();
  renderGallery();
  document.getElementById('app').classList.remove('hidden');
}

