// ── Load cards ────────────────────────────────────────────────────────────────
async function loadCards() {
  if (!USING_SUPABASE) return; // use cards.js defaults
  const { data, error } = await db.from(TABLE).select('*').order('year', { ascending: false });
  if (error) { console.error('Supabase load error:', error); return; }
  allCards = data || [];
}

